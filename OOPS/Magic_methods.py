# Magic methods in python are special methods that allow us to define how instances of a class behave when used with built-in python functions and operators.

# They are also known as dunger(double underscore) methods because their names begin and end with two underscores.

# Some common magic methods in python are as follows :

#    1. __init__, Which is called when an instance of the class is created
#    2. __str__, which is called when we use str() function on an instance
#    3. __eq__, which is called when we use the == operator to compare instance.